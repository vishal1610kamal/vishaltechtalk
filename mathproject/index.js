function add(a, b){
  return a + b;
}

function substraction(a, b){
  return a - b;
}

function multiply(a, b){
  return a * b;
}

function divide(a, b){
  return a / b;
}

function square(a){
  return a * a;
}

function cube(a){
  return a * a * a;
}

module.exports = {add, substraction, multiply, divide, square, cube};
